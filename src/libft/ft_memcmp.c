#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t	i;

	if (!n)
		return (0);
	i = 0;
	while (i < n)
	{
		if (*((unsigned char *) s1 + i) - *((unsigned char *) s2 + i))
		{
			if (*((unsigned char *) s1 + i) - *((unsigned char *) s2 + i) > 0)
				return (1);
			else
				return (-1);
		}
		i++;
	}
	return (0);
}
